package com.hicc.cloud.teacher.db;


public class Firend {
        private String name;
        private int imageId;
        public Firend(String name, int imageId) {
            this.name = name;
            this.imageId = imageId;
        }
        public String getName() {
            return name;
        }
        public int getImageId() {
            return imageId;
        }
    }

